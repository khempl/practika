<?php
/**
 * ============================================================
 * Файл: api/parse.php
 * Назначение: Запуск парсинга в фоновом режиме
 * 
 * Этот API-эндпоинт принимает file_id, проверяет наличие файла,
 * создает задачу (job), сохраняет её начальное состояние
 * и запускает фоновый процесс worker.php для обработки.
 * 
 * Ожидает: POST-запрос с JSON { file_id: string }
 * Возвращает: JSON { success: true/false, job_id: string, message: string }
 * 
 * Важно: Используется exec() для запуска фонового процесса,
 * поэтому требуется, чтобы функция exec() не была отключена в php.ini
 * ============================================================
 */

// Устанавливаем заголовок, чтобы ответ всегда был в JSON
header('Content-Type: application/json; charset=utf-8');

// Подключаем JobStore для работы с состоянием задач
require __DIR__ . '/../JobStore.php';

// ------------------------------------------------------------
// 1. Получаем и проверяем file_id из тела запроса
// ------------------------------------------------------------
$input = json_decode(file_get_contents('php://input'), true) ?: [];
$fileId = $input['file_id'] ?? '';

// Проверяем, что file_id — это 32 символа hex (валидный идентификатор)
if (!preg_match('/^[a-f0-9]{32}$/', $fileId)) {
    echo json_encode(['success' => false, 'message' => 'Некорректный file_id']);
    exit;
}

// ------------------------------------------------------------
// 2. Проверяем, что файл существует на сервере
// ------------------------------------------------------------
$uploadsDir = __DIR__ . '/../uploads';
$filePath = $uploadsDir . '/' . $fileId . '.txt';

if (!is_file($filePath)) {
    echo json_encode(['success' => false, 'message' => 'Файл не найден на сервере, загрузите его заново']);
    exit;
}

// ------------------------------------------------------------
// 3. Создаем папку для хранения статусов задач
// ------------------------------------------------------------
$jobsDir = __DIR__ . '/../jobs';
if (!is_dir($jobsDir)) {
    mkdir($jobsDir, 0777, true);
}

// ------------------------------------------------------------
// 4. Генерируем уникальный ID задачи (32 символа hex)
// ------------------------------------------------------------
$jobId = bin2hex(random_bytes(16));

// ------------------------------------------------------------
// 5. Сохраняем начальное состояние задачи в файл
// ------------------------------------------------------------
// Сразу создаём "заготовку" job-файла, чтобы status.php 
// не отвечал "не найдена задача", пока фоновый процесс 
// только запускается (посчитать общее число строк тоже требует времени)
JobStore::save($jobId, $jobsDir, [
    'status' => 'processing',              // Статус: processing, completed, error
    'total' => 0,                          // Общее количество строк в файле
    'processed' => 0,                      // Обработано строк
    'success' => 0,                        // Успешно добавлено в БД
    'errors' => 0,                         // Ошибок при парсинге
    'progress' => 0,                       // Прогресс в процентах (0-100)
    'error_groups' => [],                  // Группировка ошибок по полям
    'logs' => [                            // Лог-сообщения для вывода пользователю
        ['message' => '📤 Задача принята, запускается обработка...', 'type' => 'info']
    ],
]);

// ------------------------------------------------------------
// 6. Проверяем, что функция exec() доступна
// ------------------------------------------------------------
if (!function_exists('exec')) {
    echo json_encode(['success' => false, 'message' => 'Функция exec() отключена на сервере (disable_functions в php.ini) — фоновая обработка невозможна']);
    exit;
}

// ------------------------------------------------------------
// 7. Запускаем фоновый процесс worker.php
// ------------------------------------------------------------
$phpBin = '/usr/local/bin/php';                    // Путь к PHP CLI
$workerScript = __DIR__ . '/../worker.php';        // Путь к скрипту-воркеру

// Формируем команду: запускаем worker с аргументами file_id и job_id
// > /dev/null 2>&1 — перенаправляем весь вывод в никуда (чтобы процесс не блокировал HTTP-ответ)
// & — запускаем в фоновом режиме (демонизация)
$cmd = escapeshellcmd($phpBin) . ' ' . escapeshellarg($workerScript)
    . ' ' . escapeshellarg($fileId)
    . ' ' . escapeshellarg($jobId)
    . ' > /dev/null 2>&1 &';

exec($cmd);

// ------------------------------------------------------------
// 8. Возвращаем успешный ответ с ID задачи
// ------------------------------------------------------------
echo json_encode(['success' => true, 'job_id' => $jobId]);