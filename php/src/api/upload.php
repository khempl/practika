<?php
/**
 * ============================================================
 * Файл: api/upload.php
 * Назначение: Загрузка файла на сервер
 * 
 * Этот API-эндпоинт принимает POST-запрос с файлом,
 * проверяет его на ошибки загрузки, сохраняет в папку uploads/
 * и возвращает уникальный идентификатор file_id.
 * 
 * Ожидает: POST-запрос с полем 'file' (тип: файл)
 * Возвращает: JSON { success: true/false, file_id: string, message: string }
 * ============================================================
 */

// Устанавливаем заголовок, чтобы ответ всегда был в JSON
header('Content-Type: application/json; charset=utf-8');

// ------------------------------------------------------------
// 1. Проверяем, что файл был отправлен
// ------------------------------------------------------------
if ($_SERVER['REQUEST_METHOD'] !== 'POST' || !isset($_FILES['file'])) {
    echo json_encode(['success' => false, 'message' => 'Файл не получен']);
    exit;
}

$file = $_FILES['file'];

// ------------------------------------------------------------
// 2. Проверяем ошибки загрузки
// ------------------------------------------------------------
if ($file['error'] !== UPLOAD_ERR_OK) {
    // Карта соответствия кодов ошибок и текстовых сообщений
    $map = [
        UPLOAD_ERR_INI_SIZE   => 'Файл превышает максимально допустимый размер (upload_max_filesize)',
        UPLOAD_ERR_FORM_SIZE  => 'Файл превышает максимально допустимый размер формы',
        UPLOAD_ERR_PARTIAL    => 'Файл был загружен только частично',
        UPLOAD_ERR_NO_FILE    => 'Файл не был загружен',
    ];
    echo json_encode(['success' => false, 'message' => $map[$file['error']] ?? ('Код ошибки загрузки: ' . $file['error'])]);
    exit;
}

// ------------------------------------------------------------
// 3. Создаем папку для хранения файлов, если её нет
// ------------------------------------------------------------
$uploadsDir = __DIR__ . '/../uploads';
if (!is_dir($uploadsDir)) {
    mkdir($uploadsDir, 0777, true);
}

// ------------------------------------------------------------
// 4. Генерируем уникальный ID файла (32 символа hex)
// ------------------------------------------------------------
$fileId = bin2hex(random_bytes(16));

// Путь, куда будет сохранен файл
$dest = $uploadsDir . '/' . $fileId . '.txt';

// ------------------------------------------------------------
// 5. Перемещаем временный файл в постоянное место
// ------------------------------------------------------------
if (!move_uploaded_file($file['tmp_name'], $dest)) {
    echo json_encode(['success' => false, 'message' => 'Не удалось сохранить файл на сервере']);
    exit;
}

// ------------------------------------------------------------
// 6. Возвращаем успешный ответ с ID файла
// ------------------------------------------------------------
echo json_encode(['success' => true, 'file_id' => $fileId]);