<?php
/**
 * ============================================================
 * Файл: api/status.php
 * Назначение: Получение статуса обработки задачи
 * 
 * Этот API-эндпоинт возвращает текущее состояние задачи по job_id.
 * Клиент опрашивает его каждые 1-2 секунды для обновления:
 * - Прогресс-бара
 * - Статистики (всего, успешно, ошибки)
 * - Логов (новые сообщения)
 * - Детализации ошибок по полям
 * 
 * Ожидает: GET-запрос с параметром job_id
 * Возвращает: JSON с состоянием задачи
 * 
 * Поддерживает параметр since — возвращает только новые логи,
 * чтобы не передавать уже показанные сообщения.
 * ============================================================
 */

// Устанавливаем заголовок, чтобы ответ всегда был в JSON
header('Content-Type: application/json; charset=utf-8');

// Подключаем JobStore для работы с состоянием задач
require __DIR__ . '/../JobStore.php';

// ------------------------------------------------------------
// 1. Получаем и проверяем job_id из GET-параметров
// ------------------------------------------------------------
$jobId = $_GET['job_id'] ?? '';

// since — количество уже показанных логов (чтобы отдавать только новые)
$since = max(0, (int) ($_GET['since'] ?? 0));

// Проверяем валидность job_id (32 символа hex)
if (!JobStore::isValidId($jobId)) {
    echo json_encode(['status' => 'error', 'message' => 'Некорректный job_id']);
    exit;
}

// ------------------------------------------------------------
// 2. Загружаем состояние задачи из файла
// ------------------------------------------------------------
$jobsDir = __DIR__ . '/../jobs';
$state = JobStore::load($jobId, $jobsDir);

// Если задача не найдена — возвращаем ошибку
if ($state === null) {
    echo json_encode(['status' => 'error', 'message' => 'Задача не найдена']);
    exit;
}

// ------------------------------------------------------------
// 3. Подготавливаем данные для ответа
// ------------------------------------------------------------
$allLogs = $state['logs'] ?? []; // Все логи задачи

// Отдаем только новые логи (начиная с индекса $since)
$newLogs = array_slice($allLogs, $since);

// ------------------------------------------------------------
// 4. Формируем и возвращаем JSON-ответ
// ------------------------------------------------------------
echo json_encode([
    'status'       => $state['status'] ?? 'processing', // Статус задачи
    'total'        => $state['total'] ?? 0,             // Всего строк
    'success'      => $state['success'] ?? 0,           // Успешно добавлено
    'errors'       => $state['errors'] ?? 0,            // Ошибок
    'progress'     => $state['progress'] ?? 0,          // Прогресс в %
    'error_groups' => $state['error_groups'] ?? [],     // Группировка ошибок
    'logs'         => array_map(                        // Новые логи (только те, что не показаны)
        fn($l) => ['message' => $l['message'], 'type' => $l['type'] ?? 'info'],
        $newLogs
    ),
    'logs_total'   => count($allLogs),                  // Общее количество логов (для обновления since)
    'message'      => $state['message'] ?? null,        // Дополнительное сообщение (если есть)
]);